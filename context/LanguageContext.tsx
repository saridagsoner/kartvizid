export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
    // Try to get from localStorage, default to 'tr'
    const [language, setLanguageState] = useState<Language>(() => {
        const saved = localStorage.getItem('app_language');
        return (saved as Language) || 'tr';
    });

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem('app_language', lang);
    };

    const t = (key: string): string => {
        const translation = translations[language]?.[key];
        return translation || translations['tr']?.[key] || key;
    };

    const resolveValue = (category: string, value: string | undefined): string => {
        if (!value) return t('card.no_city');
        
        const keyMap: Record<string, string> = {
            'workType': 'work.',
            'employmentType': 'emp.',
            'educationLevel': 'edu.',
            'graduationStatus': 'grad.',
            'militaryStatus': 'military.',
            'maritalStatus': 'marital.',
            'disabilityStatus': 'disability.',
            'travelStatus': 'travel.',
            'languageLevel': 'level.',
            'language': 'lang.',
            'experience': 'exp.',
            'noticePeriod': 'common.' // Notice periods often use common.no_problem or common.immediately
        };

        const prefix = keyMap[category];
        if (prefix) {
            const trTranslations = translations['tr'];
            if (!trTranslations) return value || '';
            
            // Look for a key that starts with the prefix and has the value in Turkish
            const foundKey = Object.keys(trTranslations).find(k => 
                k.startsWith(prefix) && trTranslations[k] === value
            );
            
            if (foundKey) {
                return t(foundKey);
            }
        }

        // Special case for notice period if not found via common prefix
        if (category === 'noticePeriod' && value) {
            const trTranslations = translations['tr'];
            if (!trTranslations) return value;
            
            const foundKey = Object.keys(trTranslations).find(k => 
                trTranslations[k] === value && (k.startsWith('common.') || k.startsWith('start_date.'))
            );
            if (foundKey) return t(foundKey);
        }

        const valKey = `val.${value}`;
        const translatedVal = t(valKey);
        if (translatedVal !== valKey) return translatedVal;

        return value;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t, resolveValue }}>
            {children}
        </LanguageContext.Provider>
    );
};

export const useLanguage = () => {
    const context = useContext(LanguageContext);
    if (context === undefined) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
};
